--creating timer with parameters (params)
function delayedCallParam(funcName, delayTime, params)
    local uniqueID = 'timer2'..numTimers
    numTimers = numTimers + 1
    Timer.create({
        identifier = uniqueID,
        function_name = funcName,
        parameters = params,
        delay = delayTime
    })
end


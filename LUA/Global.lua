--Function to delay another function by x seconds (delayTime)
function delayedCall(funcName, delayTime)
    local uniqueID = 'timer'..numTimers
    numTimers = numTimers + 1
    Timer.create({
        identifier = uniqueID,
        function_name = funcName,
        delay = delayTime
    })
end

